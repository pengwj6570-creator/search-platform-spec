/**
 * Source type enumeration for data source configuration
 */
public enum SourceType {
    /**
     * MySQL database
     */
    MYSQL,

    /**
     * PostgreSQL database
     */
    POSTGRESQL,

    /**
     * Oracle database
     */
    ORACLE,

    /**
     * File-based data source
     */
    FILE
}
